//SHUFFLE ARRAY ON RELOAD

function shuffleArray(array) {
  for (let index = array.length - 1; index > 0; index--) {
    const random = Math.floor(Math.random() * (index + 1));
    [array[index], array[random]] = [array[random], array[index]];
  }
}

//FOR EACH ARRAY OBJECT, CREATE CARD DIV AND IMG ELEMENTS

function createCard() {
  shuffleArray(images);
  images.forEach(function (image) {
    const frontImg = document.createElement("img");
    const backImg = document.createElement("img");
    const cardWrapper = document.createElement("div");
    frontImg.setAttribute("class", "front-card");
    backImg.setAttribute("class", "back-card");
    cardWrapper.setAttribute("class", "card-wrapper");
    frontImg.src = image.frontUrl;
    frontImg.alt = image.frontAlt;
    backImg.src = image.backUrl;
    backImg.alt = image.backAlt;
    memoryGame.appendChild(cardWrapper);
    cardWrapper.appendChild(frontImg);
    cardWrapper.appendChild(backImg);
  });
}

